package servicios.generarInformeGrupo;

import co.edu.javeriana.ayllu.agents.communitymanageragent.CommunityManagerAgentState;

/**
 *
 * @author AylluAdmin
 */
public class EstadoGenerarInformeGrupo extends CommunityManagerAgentState {

    private DatosGenerarInformeGrupo misDatos;

    public EstadoGenerarInformeGrupo(DatosGenerarInformeGrupo misDatos) {
        super(null,null);
        this.misDatos = misDatos;
        this.setCourseID("SIST_INT");
    }

    public DatosGenerarInformeGrupo getMisDatos() {
        return misDatos;
    }

    /**
     * @param misDatos the misDatos to set
     */
    public void setMisDatos(DatosGenerarInformeGrupo misDatos) {
        this.misDatos = misDatos;
    }

}
