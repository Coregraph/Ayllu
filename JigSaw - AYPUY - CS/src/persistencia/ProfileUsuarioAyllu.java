package persistencia;

import java.io.Serializable;

/**
 *
 * @author luis
 */
public class ProfileUsuarioAyllu implements Serializable{
    private String profileId;
    private String myRaId;
    private String mySmaId;


    public ProfileUsuarioAyllu(String id, String myRaId, String mySmaId) {
        this.profileId = id;
        this.myRaId = myRaId;
        this.mySmaId = mySmaId;

    }

    public ProfileUsuarioAyllu() {
    }
    

    public String getMyRaId() {
        return myRaId;
    }

    public void setMyRaId(String myRaId) {
        this.myRaId = myRaId;
    }

    public String getMySmaId() {
        return mySmaId;
    }

    public void setMySmaId(String mySmaId) {
        this.mySmaId = mySmaId;
    }

    public String getProfileId() {
        return profileId;
    }

    public void setProfileId(String profileId) {
        this.profileId = profileId;
    }
    


    
}
