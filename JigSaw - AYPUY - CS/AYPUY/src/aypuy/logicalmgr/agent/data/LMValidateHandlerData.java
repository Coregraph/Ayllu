package aypuy.logicalmgr.agent.data;

import BESA.Kernel.Agent.Event.DataBESA;

/**
 *
 * @author Ayllu
 */
public class LMValidateHandlerData extends DataBESA {
    private String agToReceive;
    private String guardToReceive;
    private String fileName;
    private String agToRequest;
    private String handlerId;

    /**
     * 
     * @param agToReceive
     * @param guardToReceive
     * @param fileName
     * @param agToRequest
     * @param handlerId
     */
    public LMValidateHandlerData(String agToReceive, String guardToReceive, String fileName, String agToRequest, String handlerId) {
        this.agToReceive = agToReceive;
        this.guardToReceive = guardToReceive;
        this.fileName = fileName;
        this.agToRequest = agToRequest;
        this.handlerId=handlerId;
    }

    /**
     * 
     * @return
     */
    public String getAgToReceive() {
        return agToReceive;
    }

    /**
     * 
     * @return
     */
    public String getAgToRequest() {
        return agToRequest;
    }

    /**
     * 
     * @return
     */
    public String getFileName() {
        return fileName;
    }

    /**
     * 
     * @return
     */
    public String getGuardToReceive() {
        return guardToReceive;
    }

    /**
     * 
     * @return
     */
    public String getHandlerId() {
        return handlerId;
    }
    
    
    
}
