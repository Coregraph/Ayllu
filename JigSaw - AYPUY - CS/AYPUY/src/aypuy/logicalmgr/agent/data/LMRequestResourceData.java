package aypuy.logicalmgr.agent.data;

import BESA.Kernel.Agent.Event.DataBESA;

/**
 *
 * @author Ayllu
 */
public class LMRequestResourceData extends DataBESA{
    private String agentToSendReply;
    private String guardToSendReply;
    private Object query;

    /**
     * 
     * @param agentToSendReply
     * @param guardToSendReply
     * @param query
     */
    public LMRequestResourceData(String agentToSendReply, String guardToSendReply, Object query) {
        this.agentToSendReply = agentToSendReply;
        this.guardToSendReply = guardToSendReply;
        this.query = query;
    }

    /**
     * 
     * @return
     */
    public String getAgentToSendReply() {
        return agentToSendReply;
    }

    /**
     * 
     * @return
     */
    public String getGuardToSendReply() {
        return guardToSendReply;
    }

    /**
     * 
     * @return
     */
    public Object getQuery() {
        return query;
    }

    
    
}
